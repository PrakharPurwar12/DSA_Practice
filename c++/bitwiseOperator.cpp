#include<iostream>
using namespace std;

int main(){
    int a = 7;
    int b = 4;

    cout<<"AND opertor use(&): a&b = "<<(a&b)<<endl;
    cout<<"OR opertor use(|): a|b = "<<(a|b)<<endl;
    cout<<"XOR opertor use(^): a^b = "<<(a^b)<<endl;
    cout<<"NOT opertor use(~): ~b = "<<(~b)<<endl;

    cout<<"Left Shift Operator (<<): 5<<2 = "<<(5<<2)<<endl;
    cout<<"Right Shift Operator (>>): 16>>2 = "<<(16>>2)<<endl;
}